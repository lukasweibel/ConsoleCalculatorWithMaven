package ch.bbw;

public class Calculator {
    public int addition(int value1, int value2) {
        return value1 + value2;
    }

    public int subtraction(int value1, int value2) {
        return value1 - value2;
    }

    public int division(int value1, int value2) {
        return value1 / value2;
    }
}
